      five_digit_zip = five_digit_format(zip)
      ZIPCODE_MAPPINGS.dig(five_digit_zip, :geocode)
    end

    def city_to_county(city)
      ZIPCODE_MAPPINGS.values.detect { |values| values[:city] == city }&.dig(:county)
    end

    def five_digit_format(zip)
      zip.slice(/\d{5}/)
    end
  end
